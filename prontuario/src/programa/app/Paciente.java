package programa.app;

public class Paciente {
	
	String cpf;
	String nome;
	float peso;
	float altura;
	int idade;
	
	public Paciente(String cpf, String nome, float peso, float altura, int idade) {
		this.cpf = cpf;
		this.nome = nome;
		this.peso = peso;
		this.altura = altura;
		this.idade = idade;
	}
	
	
}
